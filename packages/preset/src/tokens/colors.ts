const toTokenScale = (values: readonly string[]) =>
  Object.fromEntries(values.map((value, index) => [String(index + 1), { value }]))

export const primitiveColorTokens = Object.fromEntries(
  Object.entries(lightScales).flatMap(([name, values]) => [
    [name, toTokenScale(values)],
    [`${name}Dark`, toTokenScale(darkScales[name as ScaleName])],
  ]),
) as TokenGroup

export const primitiveScaleNames = Object.keys(lightScales) as ScaleName[]
